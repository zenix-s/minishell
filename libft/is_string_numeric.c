#include "../include/libft.h"

t_bool	ft_isdigit(int c)
{
	return (c >= '0' && c <= '9');
}

t_bool	is_string_numeric(const char *str)
{
	size_t	i;

	i = 0;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i])
	{
		if (!ft_isdigit(str[i]))
			return (FALSE);
		i++;
	}
	return (TRUE);
}
